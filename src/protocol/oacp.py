"""
OACP (OpenNewt Agent Communication Protocol) v0.1

通信协议库 - 所有 Agent 消息传递的标准格式
"""

from dataclasses import dataclass, field
from datetime import datetime
from enum import Enum
from typing import Any, Dict, List, Optional
import json


class MessageType(Enum):
    """消息类型枚举"""
    SIGNAL = "SIGNAL"  # 损伤信号：Soma → Plasticus
    BLUEPRINT = "BLUEPRINT"  # 修复蓝图：Plasticus → Effector
    EXECUTION_REPORT = "EXECUTION_REPORT"  # 执行报告：Effector → Plasticus + Mnemosyne
    QUERY = "QUERY"  # 查询历史：任意 → Mnemosyne
    HUMAN_ESCALATION = "HUMAN_ESCALATION"  # 人工干预：任意 → 人类
    TICK = "TICK"  # 夜间整理：Mnemosyne → All


class Priority(Enum):
    """优先级枚举"""
    P0 = "P0"  # 最高优先级（威胁本金安全、系统崩溃）
    P1 = "P1"  # 高优先级（影响策略执行、数据异常）
    P2 = "P2"  # 中优先级（架构改进、性能优化）


class DamageType(Enum):
    """损伤类型枚举"""
    LATENCY_ANOMALY = "延迟异常"  # API 响应 > 500ms
    RESOURCE_LEAK = "资源泄漏"  # 内存/CPU 持续增长
    BEHAVIOR_DRIFT = "行为漂移"  # 测试通过率下降
    DEPENDENCY_DECAY = "依赖腐化"  # 安全漏洞、过期依赖
    CODE_DECAY = "代码腐化"  # 复杂度超标


@dataclass
class OACPMessage:
    """OACP 消息基类"""
    type: MessageType
    source: str
    target: str
    timestamp: str = field(default_factory=lambda: datetime.utcnow().isoformat() + "Z")
    payload: Dict[str, Any] = field(default_factory=dict)
    message_id: Optional[str] = None
    
    def to_json(self) -> str:
        """序列化为 JSON"""
        return json.dumps({
            "type": self.type.value,
            "source": self.source,
            "target": self.target,
            "timestamp": self.timestamp,
            "payload": self.payload,
            "message_id": self.message_id
        }, ensure_ascii=False, indent=2)
    
    @classmethod
    def from_json(cls, json_str: str) -> 'OACPMessage':
        """从 JSON 反序列化"""
        data = json.loads(json_str)
        return cls(
            type=MessageType(data["type"]),
            source=data["source"],
            target=data["target"],
            timestamp=data["timestamp"],
            payload=data["payload"],
            message_id=data.get("message_id")
        )
    
    def to_github_issue_title(self) -> str:
        """生成 GitHub Issue 标题"""
        return f"[OACP] {self.type.value} - {self.source} → {self.target}"
    
    def to_github_issue_body(self) -> str:
        """生成 GitHub Issue 内容"""
        return f"""```json
{self.to_json()}
```

---
*Generated by Axonewt Neural Plasticity Engine*
*Timestamp: {self.timestamp}*
"""


@dataclass
class SignalMessage(OACPMessage):
    """损伤信号消息（Soma → Plasticus）"""
    type: MessageType = field(default=MessageType.SIGNAL, init=False)
    
    @classmethod
    def create(
        cls,
        damage_type: DamageType,
        severity: Priority,
        location: str,
        symptoms: List[str],
        health_score: float,
        context: Optional[Dict[str, Any]] = None
    ) -> 'SignalMessage':
        """创建损伤信号消息"""
        return cls(
            source="Soma-Dev",
            target="Plasticus-Dev",
            payload={
                "damage_type": damage_type.value,
                "severity": severity.value,
                "location": location,
                "symptoms": symptoms,
                "health_score": health_score,
                "context": context or {}
            }
        )


