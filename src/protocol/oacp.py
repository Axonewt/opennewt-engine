        return cls(
            source="Effector-Dev",
            target="Plasticus-Dev",  # 也会发送给 Mnemosyne
            payload={
                "plan_id": plan_id,
                "status": status,
                "steps_completed": steps_completed,
                "steps_total": steps_total,
                "errors": errors or [],
                "health_after": health_after
            }
        )


